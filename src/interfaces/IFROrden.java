    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        DetallePedido orden = new DetallePedido ();
        Pedido pedido = new Pedido();
        
        orden.setColor(cmbcolor.getSelectedItem().toString().trim());
        orden.setTalla(Integer.parseInt(cmbtalla.getSelectedItem().toString()));
        orden.setCantidadEnDocenas(Integer.parseInt(txtcantidad.getText().trim()));
        orden.setNroTaco(Integer.parseInt(cmbtaco.getSelectedItem().toString()));
        
        int idPedido = Integer.parseInt(txtidpedido.getText());
        int idModelo = Integer.parseInt(txtidmodelo.getText());
        
        orden.registrarDetallePedido(idPedido,idModelo);
        
        pedido.setEstado(cmbestado.getSelectedItem().toString());
        pedido.actualizarEstadoPedido(Integer.parseInt(txtidpedido.getText()));
        mostrarPedido();

    }//GEN-LAST:event_btnregistrarActionPerformed

    
       public void mostrarPedido() {
        DefaultTableModel modelo = ClienteService.mostrarDetallePedido();
        tabla.setModel(modelo);
    }
    
    
    
