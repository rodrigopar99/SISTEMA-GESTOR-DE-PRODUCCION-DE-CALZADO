    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed

            int fila = tabla.getSelectedRow(); //tomamos  la fila seleccionada de la tabla
            //verificamos que se haya seleccionado una fila
            if(fila>=0){
                //enviamos al metodo eliminar registro los parametros (id de cliente que se capto al hacer click
                //en la tabla, nombre de la tabla, lo que se quiere filtrar en la consulta)
                boolean verificar = ClienteService.eliminarRegistro(idCliente,"cliente","id_cliente");
                //si todo es correcto
                if (verificar=true){
                    //actualiza la tabla
                    mostrarClientes();
                    //limpia campos previamente seleccionador
                    limpiar ();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Ninguna fila seleccionada", "Seleccione una fila", 0);
            }                   
    }//GEN-LAST:event_btneliminarActionPerformed

