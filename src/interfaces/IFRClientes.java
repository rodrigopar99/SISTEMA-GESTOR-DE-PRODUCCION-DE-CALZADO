    private boolean validarCliente(){
          boolean d =false;
          //validamos los campos vacios
         if(txtNombre.getText().equals("") || txtApPaterno.getText().equals("")|| txtApMaterno.getText().equals("")||txtTelefono.getText().equals("")||txtnroD.getText().equals("")||txtdireccion.getText().equals("")){
         JOptionPane.showMessageDialog(null, "Por favor llenar campos", "Aviso!", JOptionPane.WARNING_MESSAGE);
         }else {
             //validamos que sean numeros
             if(ClienteService.validarNumeros(txtTelefono.getText().trim()) && ClienteService.validarNumeros(txtnroD.getText().trim())){
                 //validamos que sea 8 numeros   
                 if(txtnroD.getText().trim().length()==8){
                    d=true;
                    }else{JOptionPane.showMessageDialog(null, "¡¡Error!! El DNI debe contener  8 numeros", "Aviso!", JOptionPane.WARNING_MESSAGE);}
                }else {
                    JOptionPane.showMessageDialog(null, "Colocar numeros en los campos correspondientes", "Aviso!", JOptionPane.WARNING_MESSAGE);
               }
         }
    return d;
    }
    
    
    
    
    
    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
      cliente=new Cliente();
     if (validarCliente()){
      cliente.setTelefono(txtTelefono.getText());
      cliente.setDocIdentidad(txtnroD.getText());
      cliente.setNombre(txtNombre.getText().trim().toUpperCase());
      cliente.setApPaterno(txtApPaterno.getText().trim().toUpperCase());
      cliente.setApMaterno(txtApMaterno.getText().trim().toUpperCase()); 
      cliente.setTipoDoc((String) cmbTipoDoc.getSelectedItem());
      cliente.setDireccion(txtdireccion.getText().trim().toUpperCase());
      cliente.registrarCliente();
      mostrarClientes();
      limpiar (); 
     }
      
    }//GEN-LAST:event_btnRegistrarActionPerformed

