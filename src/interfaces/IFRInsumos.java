    private boolean validarInsumo(){
    
         boolean d =false;
         if(txtnombre.getText().equals("") || txtcantidad.getText().equals("")|| txtcosto.getText().equals("")){
             
         JOptionPane.showMessageDialog(null, "Por favor llenar campos", "Aviso!", JOptionPane.WARNING_MESSAGE);
         }else {
            if(txtidproveedor.getText().equals("")){ 
                    JOptionPane.showMessageDialog(null, "Escoger un proveedor", "Aviso!", JOptionPane.WARNING_MESSAGE);
            }else {
                 if(ClienteService.validarNumeros(txtcantidad.getText().trim()) && ClienteService.validarDecimales(txtcosto.getText().trim())){
                    if( ClienteService.validarNocetosAlinicio(txtcantidad.getText().trim())){
                    d=true;
                    }else{JOptionPane.showMessageDialog(null, "¡¡Error!! Verifique cantidad", "Aviso!", JOptionPane.WARNING_MESSAGE);}
                }else {
                    JOptionPane.showMessageDialog(null, "Colocar numeros en los campos correspondientes", "Aviso!", JOptionPane.WARNING_MESSAGE);
               }
            
            
            }
         }
         
         
         
    return d;
    }
    
    
    

