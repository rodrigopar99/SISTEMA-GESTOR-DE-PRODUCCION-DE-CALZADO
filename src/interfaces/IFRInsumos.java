    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
       proveedor = new Proveedor();
       insumo=new Insumo();
       
       if (validarInsumo()){
       proveedor.setRazonSocial(txtrzproveedor.getText().trim().toUpperCase());
       proveedor.setRuc(txtrucproveedor.getText().trim());
       proveedor.setTelefono(txttelefono.getText());
       proveedor.setDireccion(txtdireccion.getText().trim().toUpperCase());
       insumo.setNombre(txtnombre.getText().trim().toUpperCase());
       insumo.setCantidad(Integer.parseInt(txtcantidad.getText().trim()));
       insumo.setPrecio(Float.parseFloat(txtcosto.getText().trim()));
       int idProveedor=Integer.parseInt(txtidproveedor.getText().trim());
       insumo.registrarInsumo(idProveedor);
       mostrarInsumos();
       limpiar();
       }
    }//GEN-LAST:event_btnRegistrarActionPerformed

      public void mostrarInsumos() {
        DefaultTableModel modelo = ClienteService.mostrarInsumos();
        tabla.setModel(modelo);
    }
    
