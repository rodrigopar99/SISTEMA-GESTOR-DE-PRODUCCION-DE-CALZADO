    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         int fila = tabla.getSelectedRow();
            if(fila>=0){
                boolean verificar = ClienteService.eliminarRegistro(idModelo,"modelo","id_modelo");
                if (verificar=true){
                    mostrarModelo();
                    limpiar ();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Ninguna fila seleccionada", "Seleccione una fila", 0);
            }      
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        String idcadena=tabla.getValueAt(tabla.getSelectedRow(),0).toString();
        idModelo=Integer.parseInt(idcadena);
        txtNombre.setText(tabla.getValueAt(tabla.getSelectedRow(),1).toString());
        cmbTipoModelo.setSelectedItem(tabla.getValueAt(tabla.getSelectedRow(),2).toString());
    }//GEN-LAST:event_tablaMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
           modelo=new Modelo();
        if(validarModelo()){
        modelo.setNombre(txtNombre.getText().trim().toUpperCase());
        modelo.setTipoModelo(cmbTipoModelo.getSelectedItem().toString());
        modelo.actualizarModelo(idModelo);
        mostrarModelo();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

