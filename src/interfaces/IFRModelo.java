    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        
        if(validarModelo()){
        modelo=new Modelo();
        modelo.setNombre(txtNombre.getText().trim().toUpperCase());
        modelo.setTipoModelo((String) cmbTipoModelo.getSelectedItem());
        modelo.registrarModelo();
        mostrarModelo();
        limpiar ();
        
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

    
     private void limpiar (){
      txtNombre.setText("");
      cmbTipoModelo.setSelectedIndex(0);
     }
    public void mostrarModelo() {
        DefaultTableModel modelo = ClienteService.mostrarModelo();
        tabla.setModel(modelo);
    }
        
        //buscar clientes
        public void buscarModelo(String buscar){
        DefaultTableModel modelo = ClienteService.buscarModelo(buscar);
        tabla.setModel(modelo);   
    }
    
    
